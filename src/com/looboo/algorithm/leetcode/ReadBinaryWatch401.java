    List<List<Integer>> combinations;

    public List<String> readBinaryWatch(int num) {

        combinations = new ArrayList<>();
        combine(0, num, new Stack<>());

        return decodeFromList(combinations);
    }

    private List<String> decodeFromList(List<List<Integer>> combinations) {

        List<String> result = new ArrayList<>();
        for (int i = 0; i < combinations.size(); i++) {
            List<Integer> list = combinations.get(i);
            int hour = 0;
            int min = 0;

            for (int j = 0; j < list.size(); j++) {
                if (list.get(j) < 4) {
                    hour += (int) Math.pow(2, list.get(j));
                } else {
                    min += (int) Math.pow(2, list.get(j) - 4);
                }
            }

            if (hour>=12 || min >= 60) continue;
            result.add(String.format("%d:%02d", hour, min));
        }

        return result;
    }


    private void combine(int start, int num, Stack<Integer> stack) {
        if (num == 0) {
            List<Integer> list = new ArrayList<>();
            list.addAll(stack);
            combinations.add(list);
            return;
        }

        for (int i = start; i < 10; i++) {
            stack.push(i);
            combine(i + 1, num - 1, stack);
            stack.pop();
        }
    }

